// MÉTODOS Y PROPIEDADES DE LAS CADENAS
let cadena = "Hola";

console.log(cadena);

// Muestra la longitud de la cadena con .length
console.log(cadena.length);

// Devuelve la letra que se ocupa en la posición especificada con .charAt()
console.log(cadena.charAt(0));

// El método .split() se utiliza para separar cadenas de la forma especificada, devolviendo un array.
alumno = "Manuel Chillón Prieto";

console.log(alumno);
console.log(alumno.split());
console.log(alumno.split(" "));
console.log(alumno[0].split());